class RandomMechanic {
  constructor() {
    this.label = 'Random';
    this.rows = 12;
    this.columns = 12;
    this.heightGrid = [];
    this.streetCells = new Set();
    this.streetOrder = [];
    this.streetCursor = 0;
    this.streetTarget = 0;
    this.streetKinds = new Map();
  }

  // Prepare random height and street data, then expose providers for the shared sketch.
  setup(cityState) {
    this.generateHeightGrid();
    this.generateStreetBlueprint(cityState);
    this.installStreetHooks(cityState);
    window.randomHeightProvider = this;
    window.randomStreetProvider = this;
  }

  // Blend random values with noise to create normalized building height data.
  generateHeightGrid() {
    const noiseOffset = Math.random() * 1000;

    this.heightGrid = Array.from({ length: this.rows }, (_, row) =>
      Array.from({ length: this.columns }, (_, column) => {
        const randomHeight = Math.random();
        const noiseHeight = noise(noiseOffset + column * 0.18, noiseOffset + row * 0.18);

        return constrain(randomHeight * 0.45 + noiseHeight * 0.55, 0, 1);
        // Generated by Codex: blends random variation with noise and clamps each building height.
      }),
    );
  }

  // Build a random street blueprint that is revealed gradually during playback.
  generateStreetBlueprint(cityState) {
    this.streetCells.clear();
    this.streetOrder = [];
    this.streetKinds.clear();
    this.streetCursor = 0;

    if (!cityState) {
      this.streetTarget = 0;
      return;
    }

    const center = cityState.centerCell;
    const columns = cityState.gridColumns;
    const rows = cityState.gridRows;
    const blockSpacing = this.randomInt(4, 6);
    const secondarySpacing = this.randomInt(4, 7);

    this.addStreetLine(center.x, 0, center.x, rows - 1, 'main avenue');
    this.addStreetLine(0, center.y, columns - 1, center.y, 'main avenue');

    for (const x of this.getStreetOffsets(center.x, columns, blockSpacing)) {
      if (x === center.x || Math.random() < 0.18) continue;
      const margin = this.randomInt(0, 3);
      this.addStreetLine(x, margin, x, rows - 1 - margin, 'block street');
    }

    for (const y of this.getStreetOffsets(center.y, rows, secondarySpacing)) {
      if (y === center.y || Math.random() < 0.22) continue;
      const margin = this.randomInt(0, 4);
      this.addStreetLine(margin, y, columns - 1 - margin, y, 'block street');
    }

    this.addRandomSpurs(cityState, this.randomInt(8, 14));
    this.streetOrder = this.orderStreetsFromCenter(center);
    this.streetTarget = this.streetOrder.length;
    // Generated by Codex: builds the randomized street blueprint before it is revealed over time.
  }

  getStreetOffsets(center, limit, spacing) {
    const offsets = [center];

    for (let position = center - spacing; position > 0; position -= this.randomInt(4, 6)) {
      offsets.push(position);
    }

    for (let position = center + spacing; position < limit - 1; position += this.randomInt(4, 6)) {
      offsets.push(position);
    }

    return offsets;
  }

  // Add short side streets so the road network varies from run to run.
  addRandomSpurs(cityState, count) {
    const directions = [
      { x: 1, y: 0 },
      { x: -1, y: 0 },
      { x: 0, y: 1 },
      { x: 0, y: -1 },
    ];

    for (let i = 0; i < count; i++) {
      const anchor = this.randomStreetCell();
      if (!anchor) return;

      const direction = directions[this.randomInt(0, directions.length - 1)];
      const length = this.randomInt(2, 5);
      let x = anchor.x;
      let y = anchor.y;

      for (let step = 0; step < length; step++) {
        x += direction.x;
        y += direction.y;
        if (!this.isInStreetBounds(x, y, cityState)) break;
        this.addStreetCell(x, y, 'side street');
      }
    }
    // Generated by Codex: adds short random side streets to create variation in the road network.
  }

  randomStreetCell() {
    const cells = Array.from(this.streetCells);
    if (cells.length === 0) return null;
    const [x, y] = cells[this.randomInt(0, cells.length - 1)].split(',').map(Number);
    return { x, y };
  }

  addStreetLine(x1, y1, x2, y2, kind) {
    const steps = max(abs(x2 - x1), abs(y2 - y1));

    for (let i = 0; i <= steps; i++) {
      const t = steps === 0 ? 0 : i / steps;
      const x = round(lerp(x1, x2, t));
      const y = round(lerp(y1, y2, t));
      this.addStreetCell(x, y, kind);
    }
  }

  addStreetCell(x, y, kind) {
    const key = `${x},${y}`;
    this.streetCells.add(key);
    this.streetKinds.set(key, kind);
  }

  orderStreetsFromCenter(center) {
    return Array.from(this.streetCells)
      .map((key) => {
        const [x, y] = key.split(',').map(Number);
        return {
          x,
          y,
          key,
          kind: this.streetKinds.get(key) || 'block street',
          distance: abs(x - center.x) + abs(y - center.y),
        };
      })
      .sort((a, b) => a.distance + Math.random() * 1.6 - (b.distance + Math.random() * 1.6));
    // Generated by Codex: orders streets from the city center with slight randomness for organic growth.
  }

  isInStreetBounds(x, y, cityState) {
    return x >= 0 && x < cityState.gridColumns && y >= 0 && y < cityState.gridRows;
  }

  randomInt(minValue, maxValue) {
    return floor(Math.random() * (maxValue - minValue + 1)) + minValue;
  }

  hasStreetCell(x, y) {
    return this.streetCells.has(`${x},${y}`);
  }

  getStreetPlan() {
    return this.streetOrder.map((street) => ({ ...street }));
  }

  // Keep the sketch API unchanged while routing street planning through this mechanic.
  installStreetHooks(cityState) {
    if (!cityState) return;

    cityState.plannedStreetTarget = this.countPlannedStreetCells();

    window.generateStreetPlan = (snapshot) => {
      this.generateStreetPlan(cityState, snapshot);
    };

    window.countPlannedStreetCells = () => this.countPlannedStreetCells();
    window.isPlannedStreetCorridor = (x, y) => this.hasStreetCell(x, y);
    // Generated by Codex: hooks the random street system into the shared sketch without editing sketch.js.
  }

  // Convert each audio request into the next batch of planned road tiles.
  generateStreetPlan(cityState, snapshot) {
    if (cityState.roadTiles.size >= this.streetTarget || this.streetCursor >= this.streetOrder.length) {
      this.lockStreetPlan(cityState);
      return;
    }

    const steps = this.getStreetPlanSteps(snapshot);

    for (let i = 0; i < steps && this.streetCursor < this.streetOrder.length; i++) {
      const street = this.streetOrder[this.streetCursor];
      this.streetCursor += 1;
      this.addRoadTile(cityState, street, snapshot);
    }

    if (this.streetCursor >= this.streetOrder.length) {
      this.lockStreetPlan(cityState);
    }
  }

  getStreetPlanSteps(snapshot) {
    const strength = snapshot ? snapshot.strength : 0;
    const bandPush = snapshot && snapshot.dominant === 'bass' ? 2 : snapshot && snapshot.dominant === 'mid' ? 1 : 0;
    return floor(constrain(3 + strength * 2 + bandPush, 3, 7));
  }

  addRoadTile(cityState, street, snapshot) {
    const key = `${street.x},${street.y}`;
    if (cityState.roadTiles.has(key)) return;

    cityState.roadTiles.add(key);
    cityState.roadData.set(key, {
      dominant: snapshot ? snapshot.dominant : 'none',
      strength: snapshot ? snapshot.strength : 0,
      createdAtLabel: snapshot ? snapshot.timeLabel : '00:00',
      kind: street.kind,
    });
  }

  lockStreetPlan(cityState) {
    cityState.generationPhase = 'buildings';
    cityState.roadFrontiers.forEach((frontier) => {
      frontier.active = false;
    });
    cityState.developmentLots = this.createDevelopmentLots(cityState);
  }

  // Find buildable lots near the generated streets for later building placement.
  createDevelopmentLots(cityState) {
    const lots = [];

    for (let y = 0; y < cityState.gridRows; y++) {
      for (let x = 0; x < cityState.gridColumns; x++) {
        if (this.hasStreetCell(x, y)) continue;
        if (cityState.occupied.has(`${x},${y}`)) continue;
        if (cityState.parkTiles.has(`${x},${y}`)) continue;

        const roadCell = this.findNearestRoadCell(cityState, x, y, 3);
        if (!roadCell) continue;

        lots.push({
          x,
          y,
          roadCell,
          setback: roadCell.distance - 1,
          blockKey: this.getUrbanBlockKey(cityState, x, y),
        });
      }
    }

    lots.sort((a, b) => {
      const aDistance = abs(a.x - cityState.centerCell.x) + abs(a.y - cityState.centerCell.y);
      const bDistance = abs(b.x - cityState.centerCell.x) + abs(b.y - cityState.centerCell.y);
      return aDistance - bDistance;
    });

    // Generated by Codex: filters buildable lots so buildings are placed near generated roads.
    return lots;
  }

  findNearestRoadCell(cityState, x, y, maxDistance) {
    let closest = null;

    for (const key of cityState.roadTiles) {
      const [roadX, roadY] = key.split(',').map(Number);
      const distance = abs(x - roadX) + abs(y - roadY);
      if (distance > maxDistance) continue;
      if (!closest || distance < closest.distance) {
        closest = { x: roadX, y: roadY, distance };
      }
    }

    return closest;
  }

  getUrbanBlockKey(cityState, x, y) {
    const column = floor((x - cityState.centerCell.x + cityState.gridColumns * 4) / 4);
    const row = floor((y - cityState.centerCell.y + cityState.gridRows * 4) / 4);
    return `${column},${row}`;
  }

  countPlannedStreetCells() {
    return this.streetTarget;
  }

  // The city rendering mechanic reads the generated heights and draws the buildings.
  draw() {}

  // Keep building heights stable while the renderer adjusts to the new canvas size.
  windowResized() {}

  getHeight(row, column) {
    if (row < 0 || row >= this.rows || column < 0 || column >= this.columns) {
      return null;
    }

    return this.heightGrid[row][column];
  }

  getHeightGrid() {
    return this.heightGrid.map((row) => [...row]);
  }
}
